x = int(input("Ingrese un  número entero: "))
y = int(input("Ingrese otro  número entero: "))

# Operaciones lógicas con x e y
print(f'{x} == {y} es {x == y}')
print(f'{x} != {y} es {x != y}')
print(f'{x} > {y} es {x > y}')
print(f'{x} < {y} es {x < y}\n')

# Operaciones matematicas con x e y
print(f'{x} + {y} es {x + y}')
print(f'{x} - {y} es {x - y}')
print(f'{x} // {y} es {x // y}')
print(f'{x} ** {y} es {x ** y}')